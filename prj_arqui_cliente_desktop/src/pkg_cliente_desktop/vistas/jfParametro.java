    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        this.obtenerDatos();
        data.setParCodigo(codigo);
        data.setParNombre(nombre);
        data.setParPuntajeMaximo(puntaje);
        parametro.create_XML(data);
        JOptionPane.showMessageDialog(this, "Se ha guardado con exito");
        this.limpiarDatos();
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
         this.obtenerDatos();
        data.setParCodigo(codigo);
        data.setParNombre(nombre);
        data.setParPuntajeMaximo(puntaje);
        parametro.edit_XML(data, codigo.toString());
        JOptionPane.showMessageDialog(this, "Se ha modificado con exito");
        this.limpiarDatos();
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        this.obtenerDatos();
        Response response = parametro.find_XML(Response.class,codigo.toString());
        GenericType<Parametroevaluacion> genericType = new GenericType<Parametroevaluacion>(){};
        data=(response.readEntity(genericType));
        txtCodigo.setText(data.getParCodigo().toString());
        txtNombre.setText(data.getParNombre());
        txtPuntaje.setText(data.getParPuntajeMaximo().toString());
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        this.obtenerDatos();
        parametro.remove(codigo.toString());
        JOptionPane.showMessageDialog(this, "Se ha eliminado con exito");
        this.limpiarDatos();
    }//GEN-LAST:event_btnEliminarActionPerformed

