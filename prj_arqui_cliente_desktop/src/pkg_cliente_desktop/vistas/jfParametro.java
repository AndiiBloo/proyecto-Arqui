    private void obtenerDatos(){
        if(txtCodigo.getText().isEmpty()){
            codigo = BigDecimal.ZERO;
        } else {
            codigo = BigDecimal.valueOf(Double.valueOf(txtCodigo.getText()));
        }        
        nombre = txtNombre.getText();
        if(txtPuntaje.getText().isEmpty()){
            puntaje = BigInteger.ZERO;
        } else {
            puntaje = BigInteger.valueOf(Long.valueOf(txtPuntaje.getText()));
        }        
    }
    private void limpiarDatos(){
        codigo = BigDecimal.ZERO;
        nombre = "";
        puntaje = BigInteger.ZERO;
        txtCodigo.setText("");
        txtNombre.setText(nombre);
        txtPuntaje.setText("");
    }
